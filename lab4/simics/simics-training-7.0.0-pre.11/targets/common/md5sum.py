##
## Simple md5sum implementation in Python
##
## Fast enough to compute the sum for the QSP images in
## reasonable time even on a Windows laptop. 
##
def md5sum(filename, blocksize=65536):
    import hashlib
    hash = hashlib.md5()  # nosec
    with open(filename, "rb") as f:
        for block in iter(lambda: f.read(blocksize), b""):
            hash.update(block)
    return hash.hexdigest()
